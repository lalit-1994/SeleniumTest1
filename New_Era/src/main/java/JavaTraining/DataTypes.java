package JavaTraining;

public class DataTypes {

	public static void main(String args[])
	{
		//Primitive data types
		
		//1. int 
		int a=5;
		int b=7;
		int c=-5;
		
		
		//2. double
		double a1=11.22;
		double a2=20;
		double a3=-12.11;
		
		
		//3. char
		char c1='d';
		char c2='s';
		char c3='e';
		
		
		//4. boolean
		boolean b1=true;
		boolean b2=false;
		
		//String is not a data type, String is a class 
		
		//String
		String u="Hello World";
		String u1="Hello User";
		String u2="!@#$%+_)(*&^%dfgcbchd837837e783e";
		
		
		
		
		
		
	}
	
}
